    public void tampilEdit(String kode){
        try {
            ResultSet rs = koneksi.getConnection().createStatement().executeQuery("SELECT pesan.kode_order,pesan.kode_pelanggan,nama,pesan.no_telepon,alamat,jenis_layanan,biaya,jumlah_unit,tanggal_order,tanggal_selesai,biaya_antar,biaya_jemput,biaya_hari,petugas_antar,status_order FROM pelanggan INNER JOIN pesan ON pesan.kode_pelanggan = pelanggan.kode_pelanggan INNER JOIN detail_order ON pesan.kode_order = detail_order.kode_order WHERE pesan.kode_order like '%"+kode+"%'");
            while(rs.next()){
                kodeorder.setText(rs.getString(1));
                kodepelanggan.setText(rs.getString(2));
                nama.setText(rs.getString(3));
                telepon.setText(rs.getString(4));
                alamat.setText(rs.getString(5));
                layanan.setSelectedItem(rs.getString(6));
                biayalayanan.setText(rs.getString(7));
                item.setText(rs.getString(8));
                biayaantar.setText(rs.getString(11));
                biayajemput.setText(rs.getString(12));
                biayahari.setSelectedItem(rs.getString(13));
                petugas.setSelectedItem(rs.getString(14));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
