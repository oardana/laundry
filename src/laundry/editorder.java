    private void ubahorderanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahorderanActionPerformed
        if(kodeorder.getText().isEmpty() || kodepelanggan.getText().isEmpty() || nama.getText().isEmpty() || telepon.getText().isEmpty() || alamat.getText().isEmpty() || layanan.getSelectedItem().equals("pilih") || biayalayanan.getText().isEmpty() || item.getText().isEmpty() || biayahari.getSelectedItem().equals(0)){
            JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong");
        }else if(telepon.getText().length() != 12){
            JOptionPane.showMessageDialog(null, "No Telepon Harus 12 Digit");
        }else{
            control_orderan.editOrder(kodeorder, kodepelanggan, nama, telepon, alamat, layanan, biayalayanan, item, biayaantar, biayajemput, biayahari, petugas);
            dispose();
            reset();
            control_orderan.refreshOrderan(admin.tabelorderan);
        }
    }//GEN-LAST:event_ubahorderanActionPerformed

